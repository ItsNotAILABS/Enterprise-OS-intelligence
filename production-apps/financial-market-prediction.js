// === Helper Functions ===

function generateMarketTimeSeries(symbol, length) {
  const data = [];
  let price = symbol === 'VIX' ? 20 : 100;

  for (let i = 0; i < length; i++) {
    const drift = symbol === 'VIX' ? -0.001 : 0.0005;
    const volatility = symbol === 'VIX' ? 0.05 : 0.015;
    const shock = Math.random() - 0.5;

    price = price * (1 + drift + volatility * shock);
    data.push(price);
  }

  return data;
}

function calculateLyapunov(data) {
  // Simplified Lyapunov calculation
  let sum = 0;
  for (let i = 1; i < Math.min(data.length, 100); i++) {
    const diff = Math.abs(data[i] - data[i-1]);
    if (diff > 0) {
      sum += Math.log(diff);
    }
  }
  return (sum / 100) * PHI;
}

function reconstructAttractor(data, dimension, delay) {
  const embedding = [];
  for (let i = 0; i < data.length - (dimension - 1) * delay; i++) {
    const point = [];
    for (let j = 0; j < dimension; j++) {
      point.push(data[i + j * delay]);
    }
    embedding.push(point);
  }
  return embedding;
}

function estimateFractalDim(embedding) {
  return 1.5 + Math.random() * 0.5; // Simplified
}

function classifyAttractor(embedding) {
  const dim = estimateFractalDim(embedding);
  if (dim < 1.3) return 'Fixed Point';
  if (dim < 2.0) return 'Limit Cycle';
  return 'Strange Attractor';
}

function calculateCenter(embedding) {
  const center = Array(embedding[0].length).fill(0);
  for (const point of embedding) {
    for (let i = 0; i < point.length; i++) {
      center[i] += point[i];
    }
  }
  return center.map(c => c / embedding.length);
}

function distance(p1, p2) {
  return Math.sqrt(p1.reduce((sum, val, i) => sum + (val - p2[i]) ** 2, 0));
}

function calculateFieldCorrelation(data1, data2) {
  const n = Math.min(data1.length, data2.length);
  const mean1 = data1.slice(0, n).reduce((s, v) => s + v, 0) / n;
  const mean2 = data2.slice(0, n).reduce((s, v) => s + v, 0) / n;

  let num = 0, den1 = 0, den2 = 0;
  for (let i = 0; i < n; i++) {
    const diff1 = data1[i] - mean1;
    const diff2 = data2[i] - mean2;
    num += diff1 * diff2;
    den1 += diff1 ** 2;
    den2 += diff2 ** 2;
  }

  return (num / Math.sqrt(den1 * den2)) * PHI;
}

function interpretCorrelation(corr, expected) {
  const diff = Math.abs(corr - expected);
  if (diff < 0.1) return 'as expected';
  if (corr > expected) return 'stronger than expected';
  return 'weaker than expected';
}

